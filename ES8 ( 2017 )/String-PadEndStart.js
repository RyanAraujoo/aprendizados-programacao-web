// STRING padStart = adicionar caracteres no inicio da string para atingir determinado taamanho 
// STRING padEnd = adicionar caracteres no fim da string para atingir determinado taamanho 


let defensores = "STRING"

let stringInitializer = defensores.padStart(10,"0")

console.log(`Inicio: ${stringInitializer}`)

// ---------------------------------------------
let stringfinally = defensores.padEnd(10,"0")

 console.log(`Final: ${stringfinally}`)
